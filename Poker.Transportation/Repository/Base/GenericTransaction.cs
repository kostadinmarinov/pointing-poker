using NHibernate;
using Poker.Transportation.Repository.Base.Interfaces;

namespace Poker.Transportation.Repository.Base
{
    public class GenericTransaction : IGenericTransaction
    {
        private readonly ITransaction _transaction;

        public GenericTransaction(ITransaction transaction)
        {
            _transaction = transaction;
        }

        #region IGenericTransaction Members

        public void Commit()
        {
            _transaction.Commit();
        }

        public void Rollback()
        {
            _transaction.Rollback();
        }

        public void Dispose()
        {
            _transaction.Dispose();
        }

        #endregion
    }
}